// Defining a class

public class Literals{

    public static void main(String a[]){

        // Literals
        // addition in char
        char c = 'a';
        c++;
        System.out.println(c);

        // addition in float
        float f = 56f;
        f++;
        System.out.println(f);

        // binary to int
        int i = 0b1011;
        i++;
        System.out.println(i);
    }
}